a= 10 
b= 3


print(a, "+" , b ,"=", a + b)   # Output: 10+3=13
print(a, "*" , b ,"=", a * b)   # Output: 10 * 3 = 30
print(a, "/" , b ,"=", a / b)   # Output: 10 / 3 = 3.333333333333333
print(a, "//" , b ,"=", a // b) # Output: 10 // 3=3
print(a, "%" , b ,"=", a % b)   # Output: 10 % 3 = 1
print(a, "**" , b ,"=", a ** b) # Output: 10**3= 1000
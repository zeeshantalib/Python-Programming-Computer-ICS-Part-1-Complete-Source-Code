x = 10
y = 10

# Greater than
print(x, ">", y, "=", x > y)     # Output: 10 > 10 = False
print(x, "<", y, "=", x < y)     # Output: 10 < 10 = False
print(x, "==", y, "=", x == y)   # Output: 10 == 10 = True
print(x, "!=", y, "=", x != y)   # Output: 10 != 10 = False
print(x, ">=", y, "=", x >= y)   # Output: 10 >= 10 = True
print(x, "<=", y, "=", x <= y)   # Output: 10 <= 10 = True

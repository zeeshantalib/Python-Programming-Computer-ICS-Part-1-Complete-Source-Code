# Define variables
x = True
y = False

# Logical AND
logical_and = x and y
print(x ,"and", y ,"=", logical_and) # Output: True and False =False

# Logical OR
logical_or = x or y
print(x , "or", y ,"=" , logical_or) # Output: True and False = True

# Logical NOT
logical_not_x = not x
print( "not",x,"=" , logical_not_x) # Output not True= False

logical_not_y = not y
print("not", y, "=", logical_not_y) # Output: not False= True
